  private rightIncrement: number = 3;
  private downIncrement: number = 1;

  private currentRow: number = 0;
  private currentColumn: number = 0;

  public solvePart1(): number {
    let result: number = 0;

    do {
      // result += this.walkRow();
      if(this.checkNextPos()) {
        result++;
      }
    } while (this.currentRow < this.trees.length);

    return result;
  }

  private checkNextPos(): boolean {
    this.currentRow += this.downIncrement;
    if(this.currentRow >= this.trees.length){
      return false;
    }

    let rowText: string = this.trees[this.currentRow];

    this.currentColumn += this.rightIncrement;
    if(this.currentColumn >= rowText.length){
      this.currentColumn = (this.currentColumn - rowText.length);
    }

    console.log("checkNextPos: ");
    console.log("this.currentRow: " + this.currentRow);
    console.log("this.currentColumn: " + this.currentColumn);

    if ('#' === rowText[this.currentColumn]) {
      return true;
    }

    return false;
  }
